#!/usr/bin/env python3
"""
    Calculate the sum of a list of float numbers.
"""


def sum_list(input_list: list[float]) -> float:
    """
    Calculate the sum of a list of float numbers.

    Parameters:
    input_list (List[float]): The list of float numbers to sum.
    
    Returns:
    float: The sum of the numbers in the list.
    """
    return sum(input_list)
